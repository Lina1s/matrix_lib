TEST(TestMul, basic) {
    matrix_lib::Matrix left;
    matrix_lib::Matrix right;
    matrix_lib::Matrix result;
    left = matrix_lib::Matrix{{-33, 20},
                              {-30, -7}};
    right = matrix_lib::Matrix{{-19, 30},
                               {-26, 29}};
    result = matrix_lib::Matrix{{107, -410},
                                {752, -1103}};
    EXPECT_EQ(left * right, result);

    left = matrix_lib::Matrix{{11,  29},
                              {-14, -14}};
    right = matrix_lib::Matrix{{15, 17},
                               {50, -39}};
    result = matrix_lib::Matrix{{1615, -944},
                                {-910, 308}};
    EXPECT_EQ(left * right, result);

    left = matrix_lib::Matrix{{-3, -46},
                              {24, -25}};
    right = matrix_lib::Matrix{{32, -1},
                               {50, 12}};
    result = matrix_lib::Matrix{{-2396, -549},
                                {-482,  -324}};
    EXPECT_EQ(left * right, result);

    left = matrix_lib::Matrix{{19, -11},
                              {16, 24}};
    right = matrix_lib::Matrix{{35,  -19},
                               {-31, -9}};
    result = matrix_lib::Matrix{{1006, -262},
                                {-184, -520}};
    EXPECT_EQ(left * right, result);

    left = matrix_lib::Matrix{{28, -28},
                              {23, -44}};
    right = matrix_lib::Matrix{{42,  46},
                               {-24, -4}};
    result = matrix_lib::Matrix{{1848, 1400},
                                {2022, 1234}};
    EXPECT_EQ(left * right, result);

    left = matrix_lib::Matrix{{6,  43},
                              {11, -15}};
    right = matrix_lib::Matrix{{31,  7},
                               {-41, 31}};
    result = matrix_lib::Matrix{{-1577, 1375},
                                {956,   -388}};
    EXPECT_EQ(left * right, result);

    left = matrix_lib::Matrix{{-50, 50},
                              {-38, -5}};
    right = matrix_lib::Matrix{{-45, -4},
                               {35,  -44}};
    result = matrix_lib::Matrix{{4000, -2000},
                                {1535, 372}};
    EXPECT_EQ(left * right, result);

    left = matrix_lib::Matrix{{15,  -27},
                              {-27, 0}};
    right = matrix_lib::Matrix{{-33, 3},
                               {-26, 18}};
    result = matrix_lib::Matrix{{207, -441},
                                {891, -81}};
    EXPECT_EQ(left * right, result);

    left = matrix_lib::Matrix{{-22, -24},
                              {15,  33}};
    right = matrix_lib::Matrix{{49, -18},
                               {25, -14}};
    result = matrix_lib::Matrix{{-1678, 732},
                                {1560,  -732}};
    EXPECT_EQ(left * right, result);

    left = matrix_lib::Matrix{{-31, 43},
                              {-24, 18}};
    right = matrix_lib::Matrix{{14,  -33},
                               {-28, 21}};
    result = matrix_lib::Matrix{{-1638, 1926},
                                {-840,  1170}};
    EXPECT_EQ(left * right, result);
}

